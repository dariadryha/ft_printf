#include "libft.h"

int		ft_str_digit(char *str)
{
	int j;

	j = 0;
	if (str[j] == '-' && str[j + 1])
		j++;
	while (str[j])
	{
		if (!ft_isdigit(str[j]))
			return (0);
		str++;
	}
	return (1);
}
